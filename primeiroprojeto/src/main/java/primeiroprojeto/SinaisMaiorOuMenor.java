package primeiroprojeto;

public class SinaisMaiorOuMenor {
	public static void main(String[] args) {

		int n1 = 2;
		int n2 = 2;
		boolean pergunta = true;

		if (pergunta) {
			System.out.println("PERGUNTA VERDADEIRA");
		}
		if (!pergunta) {
			System.out.println("PERGUNTA FALSA");
		}

		if (n1 > n2) {
			System.out.println("N1 MAIOR QUE N2");
		}
		if (n1 < n2) {
			System.out.println("N1 MENOR QUE N2");

		}
		if (n1 == n2) {
			System.out.println("N1 IGUAL A N2");
		}
		if (n1 != n2) {
			System.out.println("N1 DIFERENTE DE N2");
		}

	}

}
