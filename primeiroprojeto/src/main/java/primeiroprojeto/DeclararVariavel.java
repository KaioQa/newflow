package primeiroprojeto;

public class DeclararVariavel {
	
	public static void main(String[] args) {
		
		
		int numeroInteiro;
		double numeroQueabrado;
		boolean verdadeiroFalso;
		
		
		numeroInteiro = 100;
		System.out.println(numeroInteiro);
		numeroQueabrado = 100.100;
		System.out.println(numeroQueabrado);
		verdadeiroFalso = true;
		System.out.println(verdadeiroFalso);
		
		
		
		
		
		
		
		
		
	}

}
